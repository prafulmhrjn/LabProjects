''' 2. Write a program that reads the length of the base and the height of a right-angled triangle and prints the
 area. Every number is given on a seperate line. '''


base = float(input("Enter the length of the base of the right-angled triangle:"))
height = float(input("Enter the height of the right-angled triangle:"))
Area = (base*height)/2
print(f"The area of the right angled triangle is:{Area}")